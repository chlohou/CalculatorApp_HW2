import Foundation

class Compute {
    
    func add(a: Double, b: Double) -> Double {
        let answer = a + b
        return answer
    }
    
    func subtract(a: Double, b: Double) -> Double {
        let answer = a - b
        return answer
    }
    
    func multiply(a: Double, b: Double) -> Double {
        let answer = a * b
        return answer
    }
    
    func divide(a: Double, b: Double) -> Double {
        let answer = a / b
        return answer
    }
    
}
